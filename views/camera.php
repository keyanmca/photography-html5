        <h3>
            The camera 
        </h3>
        <p>
            I shoot all my stuff on a Nikon D7000 (Digital SLR) which is where most of these photos come from. Post processing in photoshop is minimal as I prefer to keep photos simple and get them right when I press the shutter button. 
        </p>
        <h3>
            Lenses and other photo gear
        </h3>
        <h4>    
            Nikon 18 &ndash; 200mm (F3.5 &ndash 5.6) VR1 Zoom Lens
        </h4>
        <p>
            Perfect for day to day shooting and very sharp. Built in VR (Vibration Reduction) means slower shutter speeds in poor light without too much motion blur. 
        </p>
        <h4>
            Tokina 11 &ndash; 16mm F2.8 Wide Angle Lens
        </h4>
        <p>
            This is the best affordable wide angle lens for crop frame Digital SLR cameras on the market. Given the crop frame multiplication factor of 1.5 for my Nikon, the actual focal length is 16 &ndash; 24mm but you can still get plenty in shot. I use this for shooting festivals and gigs.
        </p>    
        <h4>
            Nikon 35mm F1.8G
        </h4>
        <p>
            The wide aperture make this lens perfect for shooting in low light situations which is why I use it at gigs. It's a prime, meaning it doesn&#39;t zoom but distortion is minimal and images are sharp. There is plenty of back for buck with this lens.
        </p>
        <h4>
            Nikon 50mm F1.4D
        </h4>
        <p>
            Another prime and a favourite of mine because it&#39;s idea for shooting in low light and for creating portrait photos. I purchased this on Ebay in 2011 and had it modified to work with my camera.
        </p>
        <h4>
            Nikon SB&ndash;400 Speedlight
        </h4>
        <p>
            Small cheap and cheerful external flash which suits my needs 99% of the time. It&#39;s handy to be able to point it upwards so as not to wash out the subject in too much light but create the right effect.
        </p>
